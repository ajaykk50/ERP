package com.erp.salespruchase

import com.erp.salespruchase.repository.ProductRepository
import com.google.firebase.database.FirebaseDatabase
import dagger.Module
import dagger.Provides
import dagger.hilt.InstallIn
import dagger.hilt.components.SingletonComponent
import javax.inject.Singleton

@Module
@InstallIn(SingletonComponent::class)
object FirebaseModule {

    @Provides
    @Singleton
    fun provideFirebaseDatabase(): FirebaseDatabase {
        return FirebaseDatabase.getInstance()
    }

    @Provides
    @Singleton
    fun provideProductRepository(firebaseDatabase: FirebaseDatabase): ProductRepository {
        return ProductRepository(firebaseDatabase)
    }

}